package task1_4;

public class Task1_4 {
	public static void main(String[] args) {
		double arg1 = Double.parseDouble(args[0]);
		double arg2 = Double.parseDouble(args[1]);
		System.out.println("Argument 1 + Argument 2 = " + (arg1 + arg2));
		System.out.println("Argument 1 - Argument 2 = " + (arg1 - arg2));
		System.out.println("Argument 1 * Argument 2 = " + (arg1 * arg2));
		System.out.println("Argument 1 / Argument 2 = " + (arg1 / arg2));

	}

}
